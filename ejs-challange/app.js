app.get("/posts/:newPost", function(req, res) {
    const requestedTitle = req.params.newPost;
    // won't execute if posts is empty
    posts.forEach(function(post) { // anonymous func
        if (_.lowerCase(post.title) === _.lowerCase(requestedTitle)) {
            res.render("post", {postTitle: post.title, postContent:post.content});
            console.log("match found!");
        } else {
            console.log("no such post");
        }
    });
});

app.post("/compose", function(req, res) {
    const newPost = {
        title: req.body.newTitle,
        content: req.body.newPost
    };
    posts.push(newPost);
    res.redirect("/");
});








